function power3(z) %декларираме функция с името "power3", която приема аргумент z
  m = [];% създаваме празна матрица
 for i = 1:3*z % създаваме фор цикъл, който се изпълнява докъто i стане равно на 3*z
  disp(i^3); % отпечатваме полученото число в конзолата
  m = [m,i^3];% записваме резултата в матрица
 endfor 
 m % връщаме готовата матрица
endfunction